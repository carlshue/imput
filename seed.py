def fmt(mins):
    return f"{mins // 60:02d}:{mins % 60:02d}"

def run():
    mins_by_date = {}
    for line in RAW.strip().splitlines():
        p = line.split("|")
        y, mo, d = map(int, p[5].split("-"))
        mins_by_date.setdefault(date(y, mo, d), 0)
        mins_by_date[date(y, mo, d)] += int(p[8])

    base = os.path.dirname(os.path.abspath(__file__))
    os.makedirs(os.path.join(base, "dat"), exist_ok=True)

    ts       = "20260223_000000"
    filepath = os.path.join(base, "dat", f"fichajes_{ts}.csv")

    buf = io.StringIO()
    writer = csv.writer(buf)
    writer.writerow(["timestamp", "mes"] + [str(i) for i in range(1, 32)] + ["trabajado", "previsto"])

    for month in range(1, 13):
        dias  = []
        total = 0
        days_in_month = calendar.monthrange(2026, month)[1]

        for day in range(1, 32):
            if day > days_in_month:
                dias.append("")
                continue
            d = date(2026, month, day)
            if d.weekday() >= 5:
                dias.append("")
            elif d in mins_by_date:
                m = mins_by_date[d]
                total += m
                dias.append(fmt(m))
            else:
                dias.append("--:--")

        trabajado = fmt(total) if total else "--:--"
        writer.writerow([ts, MESES_ES[month]] + dias + [trabajado, PREVISTO[month]])

    with open(filepath, "wb") as f:
        f.write(encrypt_text(buf.getvalue()))

    print(f"OK -> {filepath}")

run()
